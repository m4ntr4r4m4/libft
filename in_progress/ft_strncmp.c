#include "libft.h"
int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	if (n == 0)
		return (0);
	while ((s1[i] != '\0' && s2[i] != '\0') && (i < n - 1 && s1[i] == s2[i]))
	{
		i++;
	}
	if (s1[i] == '\200' && s2[i] == '\0')
		return (1);
	return (s1[i] - s2[i]);
}
